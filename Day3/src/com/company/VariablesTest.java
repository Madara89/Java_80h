package com.company;

public class VariablesTest { // klases nosaukums vienmēr ir ar LIELO burtu
    public static void main(String[] args) {

        int num = 8; // aiz int tipa mainīgo vienmēr raksta ar mazo burtu
        int number = 35;
        int result = 100;
        int temp = 25;

        // ar tikko definētajiem variables var tālāk veidot formulas
        // kad izmanto kādu no variables, tas iekrāsojas melnā krāsā (aktivizējas)

        System.out.println(num + number);
        System.out.println(result / temp);
        System.out.println(number - temp);
        System.out.println(num * result);


    }
}
